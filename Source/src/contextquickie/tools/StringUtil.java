package contextquickie.tools;

/**
 * @author ContextQuickie
 *
 *         Utility class for string manipulation.
 */
public class StringUtil
{
  /**
   * Quotes the string.
   * 
   * @param source
   *          The source string.
   * @return The quoted string.
   */
  public static String QuoteString(String source)
  {
    return '"' + source + '"';
  }
}
